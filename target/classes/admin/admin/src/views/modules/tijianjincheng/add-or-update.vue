			} 
			this.$http({ 
				url: "tijianjincheng/page", 
				method: "get", 
				params: params 
			}).then(({ 
				data 
			}) => { 
				if (data && data.code === 0) { 
				       if(data.data.total>=crossoptnum) {
					     this.$message.error(this.$storage.get('tips'));
					       return false;
				       } else {
					 this.$http({
					   url: `tijianjincheng/${!this.ruleForm.id ? "save" : "update"}`,
